SELECT
    С.Страна AS Страна_сопредельная,  -- Столбец "Страна" в таблице Сопредельная_страна
    Р.Название AS Регион_сопредельной_страны,
    COUNT(П.ID) AS Количество_проектов
FROM
    Проект П
JOIN
    Проект_Сопредельная_страна ПС ON П.ID = ПС.Проект_id
JOIN
    Сопредельная_страна С ON ПС.Сопредельная_страна_id = С.ID
JOIN
    Регион Р ON П.Регион = Р.ID  -- Связываем через "Регион" в таблице "Проект"
GROUP BY
    С.Страна, Р.Название  -- Группируем только по стране и региону
ORDER BY
    Количество_проектов DESC;
